#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*iter;

	iter = lst;
	if (lst != NULL && f != NULL)
	{
		while (iter != NULL)
		{
			f(iter->dt);
			iter = iter -> next;
		}
	}
}
/*
Аргументы:
lst: Адрес указателя на узел.
f: Адрес функции, используемой для выполнения итерации по списку

Возвращаемое значение: нет

Описание:
Выполняет итерацию списка ’lst’ и применяет функцию
’f’ к содержимому каждого узла.
*/